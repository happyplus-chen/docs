const getTableList = ({ query }) => {
  let data = DATALIST
  const { currentPage = 1, pageSize = 10000, ...params } = query || {}
  const offset = (currentPage - 1) * pageSize

  const createFilter = (key, value) => {
    if (value && typeof value === 'string') {
      data = data.filter((row) => row[key]?.toString().indexOf(value) !== -1)
    }
  }
  if (params) {
    Object.keys(params).forEach((key) => {
      createFilter(key, params[key])
    })
  }
  return { list: data.slice(offset, offset + pageSize * 1), total: data.length }
}

